public class Operate {
    public static double addOperate(double firstNum, double secondNum) {
        return firstNum + secondNum;
    }

    public static double subtractOperate(double firstNum, double secondNum) {
        return firstNum - secondNum;
    }

    public static double multiplyOperate(double firstNum, double secondNum) {
        return firstNum * secondNum;
    }

    public static double divideOperate(double firstNum, double secondNum) {
        return firstNum / secondNum;
    }

    public static double modOperate(double firstNum, double secondNum) {
        return firstNum % secondNum;
    }
}
